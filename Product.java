package weekopdracht_cafe;

public class Product {
	private double verkoopPrijs, inkoopPrijs;
	private int aantalflessen, aantalMilliliterOpVoorraad;
	
	
	public double getVerkoopPrijs() {
		return verkoopPrijs;
	}
	public void setVerkoopPrijs(double verkoopPrijs) {
		this.verkoopPrijs = verkoopPrijs;
	}
	public double getInkoopPrijs() {
		return inkoopPrijs;
	}
	public void setInkoopPrijs(double inkoopPrijs) {
		this.inkoopPrijs = inkoopPrijs;
	}
	public int getAantalflessen() {
		return aantalflessen;
	}
	public void setAantalflessen(int aantalflessen) {
		this.aantalflessen = aantalflessen;
	}
	public int getAantalMilliliterOpVoorraad() {
		return aantalMilliliterOpVoorraad;
	}
	public void setAantalMilliliterOpVoorraad(int aantalMilliliterOpVoorraad) {
		this.aantalMilliliterOpVoorraad = aantalMilliliterOpVoorraad;
	}

}
